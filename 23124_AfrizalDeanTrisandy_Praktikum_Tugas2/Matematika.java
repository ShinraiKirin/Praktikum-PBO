public class Matematika implements Mtk{

    public int Pertambahan(int x, int y) {
        return x + y;
    }
    public int Pengurangan(int x, int y) {
        return x - y;
    }
    public int Perkalian(int x, int y) {
        return x * y;
    }
    public int Pembagian(int x, int y) {
        return x / y;
    }
}